	-- CREATION DU TABLE SPECIALIE
CREATE TABLE	CLINIQUE.SPECIALITE(
ID_SPECIALITE   INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
NOM_SPECIALITE      VARCHAR(30)   CHECK( NOM_SPECIALITE IN('GYNECOLOGUE','INFIRMIERE','CHIRURGIE','CARDIOLOGUE','PEDIATRIE','LABORATINE')),
TARIF  DECIMAL(10,2)  NOT NULL 
);


--CREATIONS DU TABLE MEDECINS

CREATE TABLE CLINIQUE.MEDECINS(
ID_MEDECINS  INT IDENTITY(1,1)  PRIMARY KEY,
MATRICULE       NVARCHAR(59)  NOT NULL CHECK (MATRICULE LIKE 'MED-[0-9][0-9][0-9][0-9]-[0-9][0-9][0-9]'),
NOM          VARCHAR(50)   NOT NULL ,
PRENOM   VARCHAR(40)  NOT NULL,
SEXE    CHAR(1)  CHECK (SEXE IN ('F','M')),
TELEPHONE     VARCHAR(20)  UNIQUE   NOT NULL,
EMAIL     NVARCHAR(50) UNIQUE  CHECK ( EMAIL LIKE '%@%.%'),
DATE_NAISSANCE     DATE NOT  NULL,
ADRESSE          VARCHAR(50) NOT NULL,
DATE_EMBAUCHE DATE  DEFAULT GETDATE() NOT NULL,
SALAIRE    DECIMAL(10,3)    NOT NULL,
ID_SPECIALITE    INT ,
CONSTRAINT FK_MEDECINS_SPECIALITE
FOREIGN KEY(ID_SPECIALITE)
REFERENCES CLINIQUE.SPECIALITE(ID_SPECIALITE)
ON DELETE CASCADE
ON UPDATE CASCADE
);

 --CREATIONS DU TABLE PATIENTS

  CREATE TABLE CLINIQUE.PATIENTS(
  ID_PATIENTS   INT IDENTITY(1,1)   PRIMARY KEY ,
  NUMERO_PATIENT VARCHAR(20) NOT NULL CHECK (NUMERO_PATIENT LIKE 'PAT-[0-9][0-9][0-9][0-9]-[0-9][0-9][0-9]'),
  NOM    VARCHAR(40)     NOT NULL,
  PRENOM  VARCHAR(20)  NOT NULL,
  SEXE    CHAR(1)   CHECK ( SEXE IN('F','M')),
  DATE_NAISSANCE   DATE  NOT NULL,
   AGE      AS(DATEDIFF (YEAR,DATE_NAISSANCE,GETDATE())),
  EMAIL    NVARCHAR(40)  UNIQUE CHECK( EMAIL LIKE'%@%.%'),
  TELEPHONE     VARCHAR(30)  UNIQUE  NOT NULL,
  ADRESSE   VARCHAR(100) NOT NULL,
  G_SANGUIN  NVARCHAR(10) NOT NULL,
  DATE_INSCRIPTIONS  DATE   DEFAULT GETDATE() NOT NULL
  );
  --CREATIONS DU TABLE  CONSULTATIONS

   CREATE TABLE CLINIQUE.CONSULTATIONS(
   ID_CONSULTATIONS     INT IDENTITY(1,1)  PRIMARY KEY,
   DATE_CONSULTATIONS     DATE  DEFAULT GETDATE(),
   MOTIF            VARCHAR(70)  NOT NULL,
   DIANGOSTIC     TEXT,
   STATUT    VARCHAR(40)   CHECK (STATUT IN('EN ATTENTE','ANNULER','TERMINER')),
   ID_MEDECINS  INT ,
   ID_PATIENTS  INT ,
   CONSTRAINT  FK_CONSULTATIONS_MEDECINS
   FOREIGN KEY (ID_MEDECINS)
   REFERENCES CLINIQUE.MEDECINS(ID_MEDECINS),
   CONSTRAINT  FK_CONSULTATIONS_PATIENTS
   FOREIGN KEY ( ID_PATIENTS)
   REFERENCES   CLINIQUE.PATIENTS(ID_PATIENTS)
   ON DELETE CASCADE
   ON UPDATE CASCADE
   );

-- CREATIONS DU TABLE MEDICAMENT

   CREATE TABLE CLINIQUE.MEDICAMENTS(
   ID_MEDICAMENTS  INT  IDENTITY(1,1)  PRIMARY KEY,
   NOM_MEDICAMENT    NVARCHAR(30)  NOT NULL,
   DCI    VARCHAR(50) ,
   CATEGORIE   VARCHAR(30) CHECK(CATEGORIE IN ('ANTIACIDES','ANTITUSSIFS','ANTIGOUTTEUX','ANTIBIOTIQUE')),
   PRIX_UNITAIRE  DECIMAL(10,2)  NOT NULL,
   STOCK   INT  DEFAULT 0
   );

   --CRATIONS DU TABLE PRECRIPTION

    CREATE TABLE CLINIQUE.PRESCRIPTION  (
	ID_PRESCRIPTION  INT IDENTITY(1,1)  PRIMARY KEY,
	POSOLOGIE   VARCHAR(30)  NOT NULL,
	DUREE_JOUR     INT NOT NULL,
	QUANTITE  INT NOT NULL,
	ID_MEDICAMENTS   INT,
	ID_CONSULTATIONS   INT,
	  CONSTRAINT FK_PRESCRIPTION_MEDICAMENTS
	  FOREIGN KEY (ID_MEDICAMENTS)
	  REFERENCES CLINIQUE.MEDICAMENTS(ID_MEDICAMENTS),
	  CONSTRAINT FK_PRESCRIPTION_CONSULTATIONS
	  FOREIGN KEY (ID_CONSULTATIONS)
	  REFERENCES CLINIQUE.CONSULTATIONS(ID_CONSULTATIONS)
	  ON DELETE CASCADE 
	  ON UPDATE CASCADE
	  );

	  --CREATIONS DU TBALE FACTURE

	  CREATE TABLE CLINIQUE.FACTURE(
	  ID_FACTURES  INT IDENTITY(1,1)  PRIMARY KEY ,
	  MONTANT_BASE    DECIMAL CHECK( MONTANT_BASE>0) NOT NULL,
	  REMISE     DECIMAL(10,5) CHECK(REMISE BETWEEN 0 AND 100) DEFAULT 0 NOT NULL,
	  MONTANT_NET   AS (MONTANT_BASE*(1-REMISE/100)) PERSISTED,
	  STATUT VARCHAR(50)   DEFAULT 'EN ATTENT' CHECK(STATUT IN('EN ATTENT','IMPAYER','PAYER')),
	  DATE_FATURE   DATE  DEFAULT GETDATE(),
	  ID_CONSULTATIONS  INT NOT NULL,
	  CONSTRAINT FK_FACTURE_CONSULTATIONS
	  FOREIGN KEY(ID_CONSULTATIONS)
	  REFERENCES CLINIQUE.CONSULTATIONS(ID_CONSULTATIONS)
	  );

	 
