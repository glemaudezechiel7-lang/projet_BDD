SELECT TOP 5 WITH TIES M.DCI AS NOM_COMMERCIAL ,M.CATEGORIE,M.PRIX_UNITAIRE 
FROM CLINIQUE.MEDICAMENTS AS M ORDER BY M.PRIX_UNITAIRE DESC;


--4.2  AGREGATION ET GROUP BY
--4.5

SELECT (M.NOM +'  '+ M.PRENOM) AS NOM_COMPLET , S.NOM_SPECIALITE AS SPECIALITE ,
COUNT (C.ID_CONSULTATIONS) AS TOTAL_CONSULTATIONS,
COUNT (CASE WHEN C.STATUT ='TERMINER' THEN 1 END ) AS CONSULTATIONS_TERMINER
FROM CLINIQUE.MEDECINS AS M INNER JOIN CLINIQUE.SPECIALITE AS S ON M.ID_SPECIALITE = S.ID_SPECIALITE
LEFT JOIN CLINIQUE.CONSULTATIONS AS C ON M.ID_MEDECINS = C.ID_MEDECINS
GROUP BY M.NOM,M.PRENOM,S.NOM_SPECIALITE
ORDER BY TOTAL_CONSULTATIONS DESC;


--4.6
SELECT 
    S.NOM_SPECIALITE AS SPECIALITE,
    COUNT(F.ID_FACTURES) AS NOMBRE_FACTURES_PAYEES,
    ROUND(SUM(F.MONTANT_NET), 2) AS CHIFFRE_AFFAIRES
FROM CLINIQUE.FACTURE AS F INNER JOIN CLINIQUE.CONSULTATIONS AS C
    ON F.ID_CONSULTATIONS = C.ID_CONSULTATIONS INNER JOIN CLINIQUE.MEDECINS AS M
    ON C.ID_MEDECINS = M.ID_MEDECINS
INNER JOIN CLINIQUE.SPECIALITE AS S
    ON M.ID_SPECIALITE = S.ID_SPECIALITE
WHERE F.STATUT = 'PAYER'
GROUP BY S.NOM_SPECIALITE
HAVING SUM(F.MONTANT_NET) > 2000
ORDER BY CHIFFRE_AFFAIRES  DESC;


--4.7
SELECT  P.SEXE ,COUNT(P.ID_PATIENTS) AS NOMBRE_PATIENTS,
ROUND (AVG(DATEDIFF(YEAR,P.DATE_NAISSANCE,GETDATE())),1)  AS  MOYENNE_AGE ,
 MIN(P.DATE_NAISSANCE)  AS PLUS_AGE,
 MAX(P.DATE_NAISSANCE) AS PLUS_JEUNE
 FROM CLINIQUE.PATIENTS AS P GROUP BY P.SEXE;


--4.8 
SELECT M.NOM_MEDICAMENT ,
M.CATEGORIE,
COALESCE(M.STOCK,0) AS STOCK ,
CASE WHEN COALESCE (M.STOCK,0 ) <20 THEN 'CRITIQUE' ELSE 'FAIBLE'
END AS NIVEAU_ALERTE FROM CLINIQUE.MEDICAMENTS AS M 
WHERE COALESCE (M.STOCK,0) <=50;

--4.9
SELECT FORMAT(C.DATE_CONSULTATIONS, 'yyyy-MM') AS Mois,
    COUNT(C.ID_CONSULTATIONS) AS TotalConsultations,
    COUNT(CASE WHEN C.STATUT = 'TERMINER' THEN 1 END) AS ConsultationsTerminees,
    ROUND(
        CAST(COUNT(CASE WHEN C.STATUT = 'TERMINER' THEN 1 END) AS FLOAT) 
        / NULLIF(COUNT(C.ID_CONSULTATIONS), 0) * 100, 
        1
    ) AS TauxRealisation
FROM CLINIQUE.CONSULTATIONS AS C
WHERE YEAR(C.DATE_CONSULTATIONS) = 2024
GROUP BY FORMAT(C.DATE_CONSULTATIONS, 'yyyy-MM')
ORDER BY Mois;

