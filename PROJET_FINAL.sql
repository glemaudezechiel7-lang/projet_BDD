--4.2

SELECT P.NUMERO_PATIENT,(P.NOM+'  '+P.PRENOM) AS NOM_COMPLET,
P.DATE_NAISSANCE,DATEDIFF(YEAR,P.DATE_NAISSANCE,GETDATE()) AS AGE FROM CLINIQUE.PATIENTS AS P 
WHERE P.DATE_NAISSANCE >'1990-01-01' ORDER BY P.DATE_INSCRIPTIONS DESC;


