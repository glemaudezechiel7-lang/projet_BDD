	  ALTER COLUMN ADRESSE NVARCHAR(MAX);
	  -- AJOUTER COLONNE STATUT DANS MEDECINS
	  ALTER TABLE CLINIQUE.MEDECINS
	  ADD STATUT VARCHAR(20) CHECK (STATUT IN ('ACTIF','INACTIF','EN CONGE','SUSPENDU'));

