--CREATION VIEW
	  CREATE VIEW CLINIQUE.VUE_CONSULTATIONS_DETAIL AS
	  SELECT C.ID_CONSULTATIONS,P.NOM + ' ' + P.PRENOM AS NOM_COMPLET_PATIENTS,
	  M.NOM AS NOM_MEDECIN,
	  S.NOM_SPECIALITE AS SPECIALITE_MEDECIN,
	  C.DATE_CONSULTATIONS,
	  C.MOTIF,C.STATUT FROM CLINIQUE.CONSULTATIONS C JOIN CLINIQUE.PATIENTS P ON C.ID_PATIENTS=P.ID_PATIENTS
	  JOIN CLINIQUE.MEDECINS M ON C.ID_MEDECINS  = M.ID_MEDECINS  JOIN CLINIQUE.SPECIALITE S ON M.ID_SPECIALITE =S.ID_SPECIALITE;

	 --AJOUT DU COLONNE DATE_FACTURE DANS TABLE Facture

	  ALTER TABLE CLINIQUE.FACTURE
ADD DATE_FACTURE DATE DEFAULT GETDATE();


 --CREATIONS VIEW
	  CREATE VIEW CLINIQUE.VUE_FACTURE_IMPAYEES  AS
	  SELECT F.ID_FACTURES, P.NOM  AS NOM_PATIENTS, P.TELEPHONE,F.MONTANT_NET, F.DATE_FACTURE
	  FROM CLINIQUE.FACTURE AS F  INNER JOIN CLINIQUE.PATIENTS P ON F.ID_CONSULTATIONS= P.ID_PATIENTS
	  INNER JOIN CLINIQUE.CONSULTATIONS AS C ON F.ID_FACTURES =C.ID_CONSULTATIONS
	  WHERE F.STATUT = 'IMPAYER';
	  

	 



	  --AJOUT DU COLONNE NOTES_INTERNES DANS LA TABLE CONSULTATIONS
