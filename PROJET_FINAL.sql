SELECT C.ID_CONSULTATIONS,C.MOTIF,C.STATUT,FORMAT(C.DATE_CONSULTATIONS,'dd/MM/yyy') AS DATE_CONSULTATIONS
FROM CLINIQUE.CONSULTATIONS AS C WHERE C.STATUT ='TERMINER' AND YEAR (C.DATE_CONSULTATIONS)=2024
ORDER BY C.DATE_CONSULTATIONS DESC ;

