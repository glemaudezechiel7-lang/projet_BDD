--5.4  REVENUE PAR MEDECINS

SELECT M.NOM ,
S.NOM_SPECIALITE AS SPECIALITE , COALESCE(SUM(F.MONTANT_NET),0) AS TOTAL_FACTURE_PAYEE
FROM CLINIQUE.MEDECINS AS M LEFT JOIN CLINIQUE.CONSULTATIONS AS C ON M.ID_MEDECINS =C.ID_MEDECINS
LEFT JOIN CLINIQUE.FACTURE AS F ON C.ID_CONSULTATIONS = F.ID_CONSULTATIONS
LEFT JOIN CLINIQUE.SPECIALITE AS S ON M.ID_SPECIALITE= S.ID_SPECIALITE
AND  F.STATUT='PAYER'
GROUP BY M.NOM,S.NOM_SPECIALITE 
ORDER BY TOTAL_FACTURE_PAYEE DESC;

--5.4 FULL OUTER JOIN
--5.5 INVENTAIRE SPECIALITES E MEDECINS

SELECT COALESCE(S.NOM_SPECIALITE,'PAS DE SPECIALITE') AS SPECILAITE,
COALESCE(M.NOM + ' ' + M.PRENOM,'PAS DE MEDECINS') AS NOM_MEDECINS 
FROM CLINIQUE.SPECIALITE AS S
FULL OUTER JOIN CLINIQUE.MEDECINS AS M
ON S.ID_SPECIALITE = M.ID_SPECIALITE ;

--5.4 AUTO-JOINT
--5.6 PATIENS AYANNT PLUSIEUR MEDECINS #TS

SELECT P.NOM + ' ' + P.PRENOM AS NOM_PATIENTS ,
COUNT (DISTINCT C.ID_MEDECINS) AS NOMBRE_DE_MEDECINS_#
FROM CLINIQUE.PATIENTS AS P 
INNER JOIN CLINIQUE.CONSULTATIONS AS C ON P.ID_PATIENTS = C.ID_PATIENTS
GROUP BY P.NOM, PRENOM HAVING COUNT( DISTINCT C.ID_MEDECINS) >=2;

--5.5 OUTER APPLY
--5.7 DERNIERE CONSULTATIONS PARPATIENT

SELECT P.NOM + ' ' + P.PRENOM AS NOM_PATIENT,
DC.DATE_CONSULTATIONS,
DC.MOTIF,
DC.NOM_MEDECINS FROM CLINIQUE.PATIENTS AS P OUTER APPLY ( SELECT TOP 1
 C.DATE_CONSULTATIONS AS DATE_CONSULTATIONS , C.MOTIF AS MOTIF,
 M.NOM + ' ' + M.PRENOM AS NOM_MEDECINS FROM CLINIQUE.CONSULTATIONS AS C 
 INNER JOIN CLINIQUE.MEDECINS AS M ON C.ID_MEDECINS = M.ID_MEDECINS 
 WHERE C.ID_PATIENTS = P.ID_PATIENTS ORDER BY C.DATE_CONSULTATIONS DESC)
 AS DC;


 --5.8  CONSULTATIONS AVEC LEUR PRESCRIPTIONS 

SELECT 
    C.DATE_CONSULTATIONS,
    P.NOM + ' ' + P.PRENOM AS NOM_PATIENT,
    PR.MEDICAMENT
FROM CLINIQUE.CONSULTATIONS AS C
INNER JOIN CLINIQUE.PATIENTS AS P 
    ON C.ID_PATIENTS = P.ID_PATIENTS
CROSS APPLY (
    SELECT M.NOM_MEDICAMENT AS MEDICAMENT
    FROM CLINIQUE.PRESCRIPTION AS PR
    INNER JOIN CLINIQUE.MEDICAMENTS AS M 
        ON PR.ID_MEDICAMENTS = M.ID_MEDICAMENTS
    WHERE PR.ID_CONSULTATIONS = C.ID_CONSULTATIONS
) AS PR
WHERE C.STATUT = 'TERMINER'
ORDER BY C.DATE_CONSULTATIONS, NOM_PATIENT;

